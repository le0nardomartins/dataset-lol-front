// Função para normalizar nome do campeão para URL
export const getChampionUrl = (championName) => {
  let normalized = championName.toLowerCase()
  
  // Casos especiais conhecidos
  const specialCases = {
    'dr. mundo': 'dr-mundo',
    'jarvan iv': 'jarvan-iv',
    'master yi': 'master-yi',
    'nunu e willump': 'nunu-e-willump',
    'tahm kench': 'tahm-kench',
    'renata glasc': 'renata-glasc',
    "k'sante": 'ksante',
    "kai'sa": 'kaisa',
    "kha'zix": 'khazix',
    "kog'maw": 'kogmaw',
    "rek'sai": 'reksai',
    "vel'koz": 'velkoz',
    "bel'veth": 'belveth',
    'aurelion sol': 'aurelion-sol',
    'cho\'gath': 'chogath'
  }
  
  if (specialCases[normalized]) {
    normalized = specialCases[normalized]
  } else {
    // Normalização geral
    normalized = normalized
      .replace(/\s+/g, '-')
      .replace(/'/g, '')
      .replace(/\./g, '')
      .replace(/[^a-z0-9-]/g, '')
  }
  
  return `https://www.leagueoflegends.com/pt-br/champions/${normalized}/`
}

